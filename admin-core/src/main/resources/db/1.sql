CREATE TABLE BK_BOOKS
(
    BOOK_ID      INTEGER  NOT NULL AUTO_INCREMENT,
    TITLE        VARCHAR(100),
    AUTHOR       VARCHAR(100),
    PUBLISHER    VARCHAR(100),
    PUBLISH_DATE DATE,
    PRICE        DECIMAL(10, 2),
    CATEGORY     VARCHAR(100),
