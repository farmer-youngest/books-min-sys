    CREATE_NAME  VARCHAR(100),
    UPDATE_NAME  VARCHAR(100),
    UPDATE_TIME  TIMESTAMP,
    CONSTRAINT BK_BOOKS_PK PRIMARY KEY (BOOK_ID)
